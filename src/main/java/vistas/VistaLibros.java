    private void actualizarTabla() throws SQLException {
   
    List<Libro> libros = new LibroControlador().findAllLibros();

    DefaultTableModel model = (DefaultTableModel) TablaLibros.getModel();
    model.setRowCount(0);  

    for (Libro libro : libros) {
        model.addRow(new Object[]{
            libro.getId(),
            libro.getTitulo(),
            libro.getAutor(),
            libro.getAñopublicacion(),
            libro.getGenero(),
            libro.getFechaLectura(),
            libro.getCalificacion(),
            libro.getReseña()
        });
    }
}

