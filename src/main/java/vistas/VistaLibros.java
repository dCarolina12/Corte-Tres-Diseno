    private void btnReseñaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReseñaActionPerformed
        // TODO add your handling code here:
        
        int filaSeleccionada = TablaLibros.getSelectedRow();

    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(this, "Seleccioná un libro de la tabla para agregar reseña y calificación.");
        return;
    }

    // Pedir calificación
    String calificacionStr = JOptionPane.showInputDialog(this, "Ingresá la calificación (número entero):");
    if (calificacionStr == null) return; // Cancelado
    int calificacion;

    try {
        calificacion = Integer.parseInt(calificacionStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "La calificación debe ser un número entero.");
        return;
    }

    // Pedir reseña
    String reseña = JOptionPane.showInputDialog(this, "Ingresá tu reseña:");
    if (reseña == null) return; // Cancelado

    // Obtener ID del libro seleccionado (asegurate que esté en la primera columna)
    int idLibro = (int) TablaLibros.getValueAt(filaSeleccionada, 0);

    // Actualizar libro con el controlador
    LibroControlador controlador = new LibroControlador();
    boolean actualizado = controlador.agregarReseñaYCalificacion(idLibro, calificacion, reseña);

    if (actualizado) {
        JOptionPane.showMessageDialog(this, "Reseña y calificación guardadas correctamente.");
        try {
            actualizarTabla();
        } catch (SQLException ex) {
            Logger.getLogger(VistaLibros.class.getName()).log(Level.SEVERE, null, ex);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Error al guardar reseña.");
    }
        
        
        
    }//GEN-LAST:event_btnReseñaActionPerformed

